    private void doSave() {
        useProxy = chkUseProxy.isSelected();
        if(chkUseProxy.isSelected()) {
            proxyServer = txtProxyServer.getText();
            useProxyAuth = chkProxyAuth.isSelected();
            if(chkProxyAuth.isSelected()) {
                proxyUser = txtProxyUser.getText();
                proxyPassword = txtProxyPassword.getPassword();
            } else {
                proxyUser = "";
                proxyPassword = new char[0];
            }
        } else {
            proxyServer = "";
            useProxyAuth = false;
            proxyUser = "";
            proxyPassword = new char[0];
        }
    }
