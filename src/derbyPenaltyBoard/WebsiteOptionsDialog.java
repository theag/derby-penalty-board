    public static String showDialog(java.awt.Frame parent, String gameName) {
        WebsiteOptionsDialog dialog = new WebsiteOptionsDialog(parent, true);
        dialog.txtGameName.setText(gameName);
        dialog.setLocationRelativeTo(parent);
        dialog.setVisible(true);
        String rv = null;
        if(dialog.result == OK) {
            rv = dialog.txtGameName.getText();
        }
        dialog.dispose();
        return rv;
    }
    
